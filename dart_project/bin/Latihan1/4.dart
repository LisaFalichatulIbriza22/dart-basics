void main() {
  // Menentukan nilai awal
  double p = 1000; // Principal (modal)
  double t = 2;    // Time (waktu) dalam tahun
  double r = 5;    // Rate of interest (tingkat bunga) dalam persen
  
  // Menghitung bunga sederhana
  double bunga = (p * t * r) / 100;
  
  // Menampilkan hasil
  print('Bunga sederhana adalah: $bunga');
}
